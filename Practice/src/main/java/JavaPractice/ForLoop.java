package JavaPractice;

public class ForLoop {
	
	public static void main(String[] args) {
		
		int num = 13;
		
		//start
		//condition
		//increase or decrease
		
		for(int i=0; i <= 20; i=i+2) {
			//System.out.println(num * i);
			System.out.println(num +" * "+ i +" = " + (num*i));
		}
		
		//4 * 1 = 4
	}

}
